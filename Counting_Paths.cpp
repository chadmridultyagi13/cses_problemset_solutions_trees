using namespace std;

void fs(int node, int p, vector<vector<int>>& adj, vector<vector<int>>& up,
        vector<int>& tin, vector<int>& tout, int& timer, int& l) {
    tin[node] = timer++;
    up[node][0] = p;
    for (int i = 1; i <= l; i++) {
        if (up[node][i - 1] != -1) {
            up[node][i] = up[up[node][i - 1]][i - 1];
        }
    }
    for (auto it : adj[node]) {
        if (it != p) {
            fs(it, node, adj, up, tin, tout, timer, l);
        }
    }
    tout[node] = timer++;
}

void height(vector<vector<int>>& adj, vector<int>& h, int node, int p) {
    for (auto it : adj[node]) {
        if (it != p) {
            h[it] = h[node] + 1;
            height(adj, h, it, node);
        }
    }
}

bool isAncestor(int u, int v, vector<int>& tin, vector<int>& tout) {
    return tin[u] <= tin[v] && tout[u] >= tout[v];
}

void fp(vector<vector<int>>& adj, vector<int>& par, int node, int p) {
    par[node] = p;
    for (auto it : adj[node]) {
        if (it != p) {
            fp(adj, par, it, node);
        }
    }
}

